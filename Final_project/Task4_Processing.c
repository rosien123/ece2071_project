// Apply moving average filter to a new value
uint16_t ApplyMovingAverage(uint16_t new_value) {
    // Subtract oldest value from sum
    if (ma_count == avg_size) {
        ma_sum -= ma_history[ma_index];
    } else {
        ma_count++;
    }
    // Add new value to history and sum
    ma_history[ma_index] = new_value;
    ma_sum += new_value;
    // Update index
    ma_index = (ma_index + 1) % avg_size;
    // Return average
    return (uint16_t)(ma_sum / ma_count);
}

// Reject outliers based on mean and threshold
uint16_t RejectOutlier(uint16_t value, uint16_t* values, uint16_t size) {
    // Calculate mean of buffer
    uint32_t sum = 0;
    for (uint16_t i = 0; i < size; i++) {
        sum += values[i];
    }
    uint16_t mean = (uint16_t)(sum / size);
    // Check if value is an outlier
    if (abs((int)value - (int)mean) > rejectionThreshold) {
        return mean;  // Replace outlier with mean
    }
    return value;  // Not an outlier
}

// Process a single sample with both filters
uint16_t ProcessSample(uint16_t sample) {
    // First apply moving average
    uint16_t ma_value = ApplyMovingAverage(sample);
    // We'll use the current window for outlier detection
    return RejectOutlier(ma_value, ma_history, ma_count);
}

// Process a buffer of samples
void ProcessBuffer(uint16_t* source, uint16_t* dest, uint16_t size, uint16_t offset) {
    for (uint16_t i = 0; i < size; i++) {
        dest[i + offset] = ProcessSample(source[i + offset]);
    }
}

